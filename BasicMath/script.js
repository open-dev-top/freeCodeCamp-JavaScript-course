// Adding Numbers
var sum = 10 + 1
console.log(sum)

// Subtracting Numbers
var difference = 10 - 1
console.log(difference)

// Multiplying Numbers
var product = 1 * 1
console.log(product)

// Dividing Numbers
var quotient = 3 / 2
console.log(quotient)

// Incrementing Numbers
var myVar = 1
myVar++
console.log(myVar)
++myVar
console.log(myVar)

var myVar2 = 1
myVar2--
console.log(myVar2)
--myVar2
console.log(myVar2)

// Multiplying Decimal
var product = 0.5 * 2.6
console.log(product) // 1.3

// Dividing Decimal
var product = 0.5 / 2.55
console.log(product) // 0.19607843137254904

